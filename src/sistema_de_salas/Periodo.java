package sistema_de_salas;

import java.util.Date;

public class Periodo {

	private Date inicio;
	private Date fim;
	
	public Date getInicio() {
		return this.inicio;
	}

	public void setInicio(Date inicio) {
		this.inicio = inicio;
	}

	public Date getFim() {
		return this.fim;
	}

	public void setFim(Date fim) {
		this.fim = fim;
	}
}
